// Retos destacados del día (rotan automáticamente)
const FEATURED_CHALLENGES = DOMINO_CHALLENGES.filter(c => c.is_featured);

// Retos por categoría
const getChallengesByCategory = (category) =>
  DOMINO_CHALLENGES.filter(c => c.category === category);

// Reto del día (basado en la fecha)
const getDailyChallenge = () => {
  const today = new Date();
  const dayIndex = Math.floor(today.getTime() / (1000 * 60 * 60 * 24)) % FEATURED_CHALLENGES.length;
  return FEATURED_CHALLENGES[dayIndex];
};

// Reto de la semana (por día de la semana)
const getWeeklyChallenge = () => {
  const dayOfWeek = new Date().getDay();
  return DOMINO_CHALLENGES.find(c => c.day_of_week === dayOfWeek) || DOMINO_CHALLENGES[0];
};

module.exports = {
  DOMINO_CHALLENGES,
  FEATURED_CHALLENGES,
  getChallengesByCategory,
  getDailyChallenge,
  getWeeklyChallenge,
};
